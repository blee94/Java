package src._00_practice_variable;

public class Var08 {
    public static void main(String[] args) {
//        정수의 범위
        byte b = 127;
//        안씀. 너무짧음.
        short s = 32767;
//        안씀. 애도 너무 작음.
        int i = 2147483647;
//        정수는 int 쓰면 됌.

        long l = 9223372036854775807L;
//        가장 긴 정수. 뒤에 L을 붙여준다.

        float f = 10.0f;
//        뒤에 f를 붙여준다. 안씀.
        double d = 10.0;
//        double 이 float 보다 더 큼. 실수는 double 쓰면 됌.

        String x = "Hello";
//        문자열은 String 쓰면 됌. ""을 사용함.

//        변수선언은 보통 camel case 를 사용. Class 도 동일.
//        ex) String userAccount = "value";

    }
}
